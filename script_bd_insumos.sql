CREATE TABLE IF NOT EXISTS `insumos` (
  `id_insumo` INT NOT NULL AUTO_INCREMENT,
  `nombre` VARCHAR(45) NOT NULL,
  `cantidad` VARCHAR(45) NOT NULL,
  PRIMARY KEY (`id_insumo`)
)
ENGINE = InnoDB;









